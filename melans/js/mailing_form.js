function mailing_form_show() {
  document.getElementById('mailing_form').style.display = "inline";
  document.getElementById('black_form').style.display = "flex";
}

function mailing_form_close() {
  document.getElementById('black_form').style.display = "none";
  document.getElementById('mailing_form').style.display = "none";
  document.getElementById('mailing_form').style.display = "none";
}
